RASTERLO = $90
RASTERHI = $92

;--------------

RASTERAT:
    SEI
    LDA TMPPTR
    STA $200
    LDA TMPPTR+1
    STA $201

    LDA $0230
    STA TMPPTR
    LDA $0231
    STA TMPPTR+1

    CLC
    LDA RASTERLO
    ADC #6
    STA RASTERLO

    LDY #5
RASTERATL2A:
    INY
RASTERATL3:
    LDA (TMPPTR),Y
    AND #$7F
    STA (TMPPTR),Y
    CPY RASTERLO
    BNE RASTERATL2A
    ORA #$80
    STA (TMPPTR),Y

    LDA #$C0
    STA $D40E
    CLI
    RTS
