; A / 2^I -> A (unsigned)
CPUDIV2CONST8U:
CPUDIV2CONST8UL1:
    SR
    DECI
    JRNZM CPUDIV2CONST8UL1
    RTN

; A:B / 2^I -> A:B (signed)
CPUDIV2CONST16S:
CPUDIV2CONST16SL1:
    PUSH
    ANIA 0x7F
    CALL CPUDIV2CONST8U
    LP 0
    EXAM    
    POP
    ANIA 0x80
    ORMA
    EXAM
    RTN
