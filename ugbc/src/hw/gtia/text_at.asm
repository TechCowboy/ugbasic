TEXTPTR = $90
TEXTSIZE = $98
TABSTODRAW = $93
SCREENCODE = $97

TEXTATDECODE:
    CMP #96
    BEQ TEXTATDECODEI1
    BCS TEXTATDECODEI1
    CMP #32
    BEQ TEXTATDECODEI0
    BCS TEXTATDECODEI0
    CLC
    ADC #64
    JMP TEXTATDECODEE
TEXTATDECODEI0:
    SEC
    SBC #32
    JMP TEXTATDECODEE
TEXTATDECODEI1:
    JMP TEXTATDECODEE
TEXTATDECODEE:
    STA SCREENCODE
    RTS

; Read a char from the text buffer to print.
; Input: TEXTPTR - pointer to the string to print
;        X - characters left to print
; Output: TEXTPTR is updated; X is updated
TEXTATREADCHAR:
    LDA (TEXTPTR),Y
    DEX
    INC TEXTPTR
    BNE TEXTATREADCHARL1
    INC TEXTPTR+1
TEXTATREADCHARL1:
    RTS

; Parse TAB character.
; Input: XCURSYS - current X cursor position
;        TABCOUNT - size of TAB spacing
; Output: TABSTODRAW - number of characters to skip
TEXTATTAB:
    LDA XCURSYS
TEXTATTAB2:
    CMP TABCOUNT
    BCC TEXTATTAB3
    SEC
    SBC TABCOUNT
    JMP TEXTATTAB2
TEXTATTAB3:
    STA TMPPTR
    LDA TABCOUNT
    SEC
    SBC TMPPTR
    STA TABSTODRAW
    RTS

