CPUUPPERCASE:
    LDY #$0
CPUUPPERCASE2:
    LDA (TMPPTR), Y
    CMP #65
    BCS CPUUPPERCASE3
    CMP #90
    BCC CPUUPPERCASE3
    CLC
@IF C128 || C64
    ADC #128
@ELSE
    ADC #32
@ENDIF
CPUUPPERCASE3:
    STA (TMPPTR2), Y
    INY
    DEX
    BNE CPUUPPERCASE2
    RTS