; Flip a string
;   - TMPPTR = address of the source string
;   - TMPPTR2 = address of the target string
;   - A = lenght of the string
CPUFLIP:
    PHA

    CLC
    ADC TMPPTR2
    STA TMPPTR2
    LDA TMPPTR2+1
    ADC #0
    STA TMPPTR2+1

    DEC TMPPTR2

    PLA
    TAX

    LDY #$0
CPUFLIPX:
    LDA (TMPPTR), Y
    STA (TMPPTR2), Y
    DEC TMPPTR2
    INC TMPPTR
    DEX
    BNE CPUFLIPX
    RTS
