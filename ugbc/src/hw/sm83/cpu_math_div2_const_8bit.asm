; B / 2^C -> B (signed)
CPUDIV2CONST8S:
    LD A, B
    AND $80
    PUSH AF
    CP 0
    JR Z, CPUDIV2CONST8SPOS

    LD A, B
    XOR $FF
    LD B, A

    INC B

CPUDIV2CONST8SPOS:
    SRA B
    DEC C
    JR NZ, CPUDIV2CONST8SPOS

    POP AF
    AND $80
    CP 0
    RET Z

    LD A, B
    XOR $FF
    LD B, A

    INC B
    RET

CPUDIV2CONST8U:
CPUDIV2CONST8UL1:
    SRL B
    DEC C
    JR NZ, CPUDIV2CONST8UL1
    RET